BEGIN TRANSACTION;
CREATE TABLE IF NOT EXISTS "Deaths" (
	"UUID"	TEXT NOT NULL PRIMARY KEY UNIQUE,
	"created"	TEXT NOT NULL DEFAULT current_timestamp,
	"world"	TEXT NOT NULL,
	"x"	INTEGER NOT NULL,
	"y"	INTEGER NOT NULL,
	"z"	INTEGER NOT NULL,
	"inventory"	TEXT NOT NULL
);
CREATE TABLE IF NOT EXISTS "Tours" (
    "ID" INTEGER PRIMARY KEY AUTOINCREMENT,
    "name" TEXT NOT NULL UNIQUE,
    "world" TEXT NOT NULL,
	"x"	INTEGER NOT NULL,
	"y"	INTEGER NOT NULL,
	"z"	INTEGER NOT NULL,
	"distance" INTEGER NOT NULL,
	"iconItemStackB64" TEXT NOT NULL
);
CREATE TABLE IF NOT EXISTS "ToursPlayers" (
    "UUID" TEXT NOT NULL PRIMARY KEY UNIQUE,
    "world" TEXT NOT NULL,
    "x"	INTEGER NOT NULL,
    "y"	INTEGER NOT NULL,
    "z"	INTEGER NOT NULL,
    "tourID" INTEGER, -- Can be nullable! Since a tour may be deleted whilst a player is offline.
    FOREIGN KEY (TourID) REFERENCES Tours(ID)
);
COMMIT;
